                <h4 class="mb10">Q：什么是品牌？</h4>
                <p class="mb50"><strong>A：</strong>英文中品牌是Brand，是烙印的意思，就是在牲畜或器物上烫
                的火印，作为标记。现代营销学之父科特勒在《市场营销学》中对品牌的的定义是：品牌是销售者向购买者长期
                提供的一组特定的特点、利益和服务。我们可以理解为品牌指消费者对产品及产品系列的认知程度。是给拥有者
                带来溢价、产生增值的一种无形资产，品牌的载体是用于和其他竞争者的产品或劳务相区分的事物及其组合，其
                作用核心在于消费者心智中形成关于品牌的印象。</p>
                <h4 class="mb10">Q：品牌有什么作用？</h4>
                <p class="mb50"><strong>A：</strong>1）<strong>品牌可以提升商品或者企业的辨识度。
                </strong>好的品牌消费者一看到就能知道这个品牌是哪家公司的，是做什么的，所以品牌设计应具有独特性
                ，有鲜明的个性特征，品牌的图案，文字等与竞争对手的区别。2）<strong>品牌可以体现商品或者企业的核
